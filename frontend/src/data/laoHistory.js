/**
 * Parse one 6-digit Lao draw number into sub-fields.
 * tail4   = full[2:]    → last 4 digits (4 ตัวท้าย)
 * top3    = full[3:]    → last 3 digits (3 ตัวบน)
 * top2    = full[4:]    → last 2 digits (2 ตัวบน)
 * bottom2 = full[2:4]   → digits at pos 2-3 (2 ตัวล่าง)
 */
function parseLaoDraw(full) {
  return {
    tail4:   full.slice(2),
    top3:    full.slice(3),
    top2:    full.slice(4),
    bottom2: full.slice(2, 4),
  };
}

